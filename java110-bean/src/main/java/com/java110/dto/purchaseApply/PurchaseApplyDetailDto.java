package com.java110.dto.purchaseApply;

import java.util.List;

//采购明细表
public class PurchaseApplyDetailDto {
    private String applyOrderId;
    private String resId;
    private String quantity;
    private String remark;
    private List<String> applyOrderIds;
    private String resName;
    private String resCode;
    private String price;
    private String stock;
    private String description;

    public String getApplyOrderId() {
        return applyOrderId;
    }

    public void setApplyOrderId(String applyOrderId) {
        this.applyOrderId = applyOrderId;
    }

    public String getResId() {
        return resId;
    }

    public void setResId(String resId) {
        this.resId = resId;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public List<String> getApplyOrderIds() {
        return applyOrderIds;
    }

    public void setApplyOrderIds(List<String> applyOrderIds) {
        this.applyOrderIds = applyOrderIds;
    }

    public String getResName() {
        return resName;
    }

    public void setResName(String resName) {
        this.resName = resName;
    }

    public String getResCode() {
        return resCode;
    }

    public void setResCode(String resCode) {
        this.resCode = resCode;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getStock() {
        return stock;
    }

    public void setStock(String stock) {
        this.stock = stock;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}


